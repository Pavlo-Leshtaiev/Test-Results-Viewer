package edu.kilabs.trv.view.roottab;

import java.util.HashMap;
import java.util.Map;

public enum TabIndex {

    // -----------------------------------------------------------------------------------------------------------------

    ABOUT(1),
    LANGUAGE(2),
    STATISTICS(3),
    TEST_RESULTS(4);

    // -----------------------------------------------------------------------------------------------------------------

    private final int val;
    private static Map<Integer, TabIndex> intToTabIndex = new HashMap<>();

    // -----------------------------------------------------------------------------------------------------------------

    TabIndex(int val){
        this.val = val;
    }

    // -----------------------------------------------------------------------------------------------------------------

    static {
        for (TabIndex ti: TabIndex.values()) {
            intToTabIndex.put(ti.getIndex(), ti);
        }
    }

    // -----------------------------------------------------------------------------------------------------------------

    public int getIndex(){
        return val;
    }

    // -----------------------------------------------------------------------------------------------------------------

    public static TabIndex valueOf(int val) {
        return intToTabIndex.get(val);
    }

    // -----------------------------------------------------------------------------------------------------------------

}
